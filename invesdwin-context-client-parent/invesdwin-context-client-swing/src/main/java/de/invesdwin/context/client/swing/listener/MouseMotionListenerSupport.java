import javax.annotation.concurrent.Immutable;

@Immutable
public class MouseMotionListenerSupport implements MouseMotionListener {

    @Override
    public void mouseDragged(final MouseEvent e) {}

    @Override
    public void mouseMoved(final MouseEvent e) {}

}
